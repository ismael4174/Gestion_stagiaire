     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nom_sess'=>'required',
            'deb_sess'=>'required',
            'fin_sess'=>'required',
        ]);

        $session=new session;
        $session->nom_sess=$request->nom_sess;
        $session->deb_sess=$request->deb_sess;
        $session->fin_sess=$request->fin_sess;
        $session->save();

        return redirect('/session/create')->with('success', 'Les informations ont été ajouter');

    }

